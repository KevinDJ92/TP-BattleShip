package views.principals;

import java.awt.Color;

import javax.swing.JButton;

public class GameButton extends JButton {
	
	boolean bntIsPressed;
	Integer indice;

	public GameButton() {
		this.bntIsPressed = false;
		this.setOpaque(false);
		this.setContentAreaFilled(false);

	}

	public Integer getIndice() {
		return indice;
	}

	public void setIndice(Integer indice) {
		this.indice = indice;
	}

	public boolean isBntIsPressed() {
		return bntIsPressed;
	}

	public void setBntIsPressed(boolean bntIsPressed) {
		this.bntIsPressed = bntIsPressed;
	}
	
	
}

